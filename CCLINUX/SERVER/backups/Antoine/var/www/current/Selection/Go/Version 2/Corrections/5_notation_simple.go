package main

import "fmt"

func im_paire(a int,  b int) {
    var c int;
	c = a + b;

	if (c % 2 == 0){
	   fmt.Print("Paire ");
	} else if (c % 2 == 1){
	   fmt.Print("Impaire ");
	}
     
	if (c < 10){
	   fmt.Print("Noire\n");
      } else if (c >= 10){	
      	   fmt.Print("Rouge\n");
     }
}

func main() {
	im_paire(1, 1);
	im_paire(42, 21);
	im_paire(8, 4);
}